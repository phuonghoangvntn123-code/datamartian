let currentLang = localStorage.getItem('lang') || 'vi';

function t(key) {
  return i18n[currentLang][key] || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.setAttribute('lang', lang === 'vi' ? 'vi' : 'en');
  translatePage();
  updateLangBtn();
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

function translatePage() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
}

function updateLangBtn() {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const lang = btn.dataset.lang;
    btn.classList.toggle('text-white', lang === currentLang);
    btn.classList.toggle('text-gray-500', lang !== currentLang);
    btn.classList.toggle('bg-violet-500/20', lang === currentLang);
    btn.classList.toggle('hover:bg-gray-800/50', lang !== currentLang);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  setLang(currentLang);
});
